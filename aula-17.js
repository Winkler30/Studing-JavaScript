// SWITCH CASE
function teste(key){
    switch (key) {
        case 'Charles':
            console.log('Charles aqui')
            break;
        case 'Julio':
            console.log('Julio aqui')
            break;
        case 'Souza':
            console.log('Souza aqui')
            break;
        default:
            console.log('Nenhum dos nomes')
            break;
    }

}
teste('Charles');
teste('Julio');
teste('Souza');

