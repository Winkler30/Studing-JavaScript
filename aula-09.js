function soma (item1,item2) {
    return item1 + item2;
}
function diminuir (item1,item2) {
    return item1 - item2;
}

console.log('Resultado da função soma:',soma(12,20));
console.log('Resultado da função diminuir:',diminuir(12,20));
// Na função podemos criar logica, e retornar o resultado
